puts "What is the weather?"
weather = gets.chomp
case weather
when 'sunny'
  puts "Yeehaw, bikini time!"
when 'cloudy'
  puts "Prepare for possiblity of rain!"
when 'foggy' 
  puts "Find the googles with the fog lamps, now!"
when 'rainy'
  puts  "Rain jacket and Rain boots!" 
when 'snowy'
  puts "Bust out the UGG's" 
else 
  puts "Forget about it, its the end of the world!"
end          
